# Color scheme (R, G, B). Used throughout generate_pdf.
_COLOR_TITLE        = (15, 50, 95)      # deep navy for cover title
_COLOR_HEADER       = (24, 78, 119)     # dark blue for ## headers
_COLOR_SUBHEADER    = (60, 110, 150)    # medium blue for ### headers
_COLOR_DEFAULT      = (40, 40, 40)      # near-black body text
_COLOR_DIM          = (110, 110, 110)   # captions / footers
_COLOR_RULE         = (180, 195, 215)   # horizontal rule line
_COLOR_TABLE_HEAD   = (220, 232, 244)   # light blue table header fill
_COLOR_TABLE_BORDER = (170, 180, 200)
_COLOR_CODE_BG      = (244, 246, 250)
_COLOR_CRITICAL     = (190, 30, 30)     # CRITICAL / VULNERABLE / CVE-
_COLOR_HIGH         = (205, 95, 25)
_COLOR_MEDIUM       = (190, 150, 30)
_COLOR_LOW          = (50, 130, 65)     # success / LOW
_COLOR_INFO         = _COLOR_SUBHEADER

# Severity label -> color. Used ONLY for explicit [CRITICAL], [HIGH], etc.
# labels and the single-word cells in the severity summary table.
# Body paragraphs stay black — no inline color.
_SEVERITY_COLORS = {
    "CRITICAL": _COLOR_CRITICAL,
    "HIGH":     _COLOR_HIGH,
    "MEDIUM":   _COLOR_MEDIUM,
    "LOW":      _COLOR_LOW,
    "INFO":     _COLOR_DIM,
}


def _severity_label_color(text: str):
    """Return the (r,g,b) for an EXACT severity word, or None.

    Only matches when the cell/label is exactly 'CRITICAL', 'HIGH', 'MEDIUM',
    'LOW', or 'INFO' — stops the body-text red-coloring problem.
    """
    return _SEVERITY_COLORS.get(text.strip().upper())


def _extract_severity_badge(line: str):
    """If line starts with '[SEVERITY] rest...', return (severity, rest) else (None, line)."""
    m = re.match(r"^\[(CRITICAL|HIGH|MEDIUM|LOW|INFO)\]\s*(.*)", line)
    if m:
        return m.group(1), m.group(2)
    return None, line


def _draw_severity_badge(pdf, severity: str):
    """Draw a small colored pill with the severity text, advance the cursor.

    Keeps the body line text black — only the badge itself is colored. Much
    cleaner than painting the entire line in red bold.
    """
    color = _SEVERITY_COLORS.get(severity.upper(), _COLOR_DIM)
    pdf.set_font("Helvetica", "B", 8)
    text = f" {severity.upper()} "
    w = pdf.get_string_width(text) + 2
    pdf.set_fill_color(*color)
    pdf.set_text_color(255, 255, 255)
    pdf.cell(w, 5, text, border=0, align="C", fill=True)
    pdf.set_text_color(*_COLOR_DEFAULT)
    pdf.set_font("Helvetica", "", 10)
    pdf.cell(2, 5, "")  # small gap


def _table_row_height(pdf, cells: list[str], col_widths: list[float], line_h: float) -> float:
    """Measure how tall a row needs to be so every cell fits, wrapped."""
    max_lines = 1
    for cell, w in zip(cells, col_widths):
        # Approximate: split by manually inserted \n then by measured width
        text = _safe(cell)
        if not text:
            continue
        # get_string_width is in the current font metric (assumed set by caller)
        n_lines = max(1, pdf.multi_cell(
            w, line_h, text, border=0, align="L",
            split_only=True, new_x="RIGHT", new_y="TOP",
        ).__len__() if hasattr(pdf, "multi_cell") else 1)
        if n_lines > max_lines:
            max_lines = n_lines
    return max_lines * line_h


def _render_table(pdf, rows: list[list[str]]):
    """Render a markdown table with proportional column widths and wrapped cells."""
    if not rows:
        return

    usable = pdf.w - pdf.l_margin - pdf.r_margin
    col_count = max(len(r) for r in rows)

    # Compute a "natural" width for each column = max string width of any cell in it
    pdf.set_font("Helvetica", "", 9)
    natural_w = [0.0] * col_count
    for row in rows:
        for i, cell in enumerate(row):
            if i >= col_count:
                continue
            # Strip markdown bold markers for measurement
            clean = re.sub(r"\*\*(.+?)\*\*", r"\1", cell)
            clean = re.sub(r"`(.+?)`", r"\1", clean)
            w = pdf.get_string_width(_safe(clean)) + 4  # padding
            if w > natural_w[i]:
                natural_w[i] = w

    total_natural = sum(natural_w)
    if total_natural <= usable:
        # Everything fits naturally — pad columns proportionally to fill width
        extra = usable - total_natural
        col_widths = [w + extra / col_count for w in natural_w]
    else:
        # Scale everything down proportionally, but cap the scale so even the
        # widest columns still fit by wrapping
        scale = usable / total_natural
        col_widths = [max(w * scale, usable / (col_count * 3)) for w in natural_w]
        # Re-normalize to exactly usable width
        s = sum(col_widths)
        col_widths = [w * (usable / s) for w in col_widths]

    line_h = 5

    # Header row
    header = rows[0]
    pdf.set_fill_color(*_COLOR_TABLE_HEAD)
    pdf.set_text_color(*_COLOR_HEADER)
    pdf.set_font("Helvetica", "B", 9)
    pdf.set_draw_color(*_COLOR_TABLE_BORDER)

    # Measure header height (wrapped)
    header_h = _wrapped_row_height(pdf, header, col_widths, line_h)
    _render_row(pdf, header, col_widths, header_h, line_h, fill=True, bold=True)

    # Body rows
    pdf.set_text_color(*_COLOR_DEFAULT)
    pdf.set_font("Helvetica", "", 9)
    for row in rows[1:]:
        row_h = _wrapped_row_height(pdf, row, col_widths, line_h)
        # Page break guard
        if pdf.get_y() + row_h > pdf.h - pdf.b_margin:
            pdf.add_page()
        _render_row(pdf, row, col_widths, row_h, line_h, fill=False, bold=False)

    pdf.set_draw_color(*_COLOR_RULE)


def _wrapped_row_height(pdf, cells: list[str], col_widths: list[float], line_h: float) -> float:
    """Calculate the row height needed to fit every cell's wrapped content."""
    max_lines = 1
    for i, cell in enumerate(cells):
        if i >= len(col_widths):
            break
        clean = re.sub(r"\*\*(.+?)\*\*", r"\1", cell)
        clean = re.sub(r"`(.+?)`", r"\1", clean)
        text = _safe(clean)
        if not text.strip():
            continue
        # Count wrapped lines: how many times will this text break at col_widths[i]?
        # Use fpdf2's built-in: split_only returns the lines list.
        try:
            lines = pdf.multi_cell(
                col_widths[i] - 2, line_h, text,
                split_only=True, new_x="RIGHT", new_y="TOP",
            )
            n = max(1, len(lines))
        except Exception:
            n = 1
        if n > max_lines:
            max_lines = n
    return max_lines * line_h


def _render_row(pdf, cells: list[str], col_widths: list[float], row_h: float,
                line_h: float, fill: bool, bold: bool):
    """Draw a single row with wrapped cells at a fixed total height."""
    x_start = pdf.get_x()
    y_start = pdf.get_y()
    for i in range(len(col_widths)):
        cell_text = cells[i] if i < len(cells) else ""
        clean = re.sub(r"\*\*(.+?)\*\*", r"\1", cell_text)
        clean = re.sub(r"`(.+?)`", r"\1", clean)
        text = _safe(clean).strip()

        # Per-cell color: severity words in the summary table, nothing else
        sev_color = _severity_label_color(text)
        if sev_color:
            pdf.set_text_color(*sev_color)
            pdf.set_font("Helvetica", "B", 9)
        else:
            if bold:
                pdf.set_font("Helvetica", "B", 9)
                pdf.set_text_color(*_COLOR_HEADER)
            else:
                pdf.set_font("Helvetica", "", 9)
                pdf.set_text_color(*_COLOR_DEFAULT)

        x_cell = x_start + sum(col_widths[:i])
        pdf.set_xy(x_cell, y_start)
        # Draw border manually so we can control height
        pdf.rect(x_cell, y_start, col_widths[i], row_h)
        if fill:
            pdf.set_fill_color(*_COLOR_TABLE_HEAD)
            pdf.rect(x_cell, y_start, col_widths[i], row_h, style="F")
            pdf.rect(x_cell, y_start, col_widths[i], row_h)
        # Draw the text wrapped inside the cell with a small top padding
        pdf.set_xy(x_cell + 1, y_start + 0.5)
        pdf.multi_cell(
            col_widths[i] - 2, line_h, text,
            border=0, align="L",
            new_x="RIGHT", new_y="TOP",
        )

    pdf.set_xy(x_start, y_start + row_h)
    pdf.set_text_color(*_COLOR_DEFAULT)
    pdf.set_font("Helvetica", "", 10)


def generate_pdf(md_content: str, pdf_path: str):
    """Convert the report content into a styled PDF with color highlighting."""
    try:
        from fpdf import FPDF
    except ImportError as e:
        raise ImportError(
            "fpdf2 is required for PDF reports. Install with: pip install fpdf2"
        ) from e

    pdf = FPDF()
    pdf.set_auto_page_break(auto=True, margin=20)
    pdf.add_page()

    # ── Cover / title ──
    pdf.set_text_color(*_COLOR_TITLE)
    pdf.set_font("Helvetica", "B", 24)
    pdf.cell(0, 15, "Network Security", new_x="LMARGIN", new_y="NEXT", align="C")
    pdf.cell(0, 12, "Assessment Report", new_x="LMARGIN", new_y="NEXT", align="C")

    pdf.set_text_color(*_COLOR_DIM)
    pdf.set_font("Helvetica", "", 11)
    pdf.cell(
        0, 6,
        f"Generated: {datetime.now().strftime('%Y-%m-%d %H:%M:%S')}",
        new_x="LMARGIN", new_y="NEXT", align="C",
    )
    pdf.ln(3)

    # Decorative rule under title
    pdf.set_draw_color(*_COLOR_HEADER)
    pdf.set_line_width(0.6)
    y = pdf.get_y()
    pdf.line(pdf.l_margin + 30, y, pdf.w - pdf.r_margin - 30, y)
    pdf.set_line_width(0.2)
    pdf.ln(6)

    # Reset to body defaults
    pdf.set_text_color(*_COLOR_DEFAULT)
    pdf.set_font("Helvetica", "", 10)

    in_code_block = False

    # Professional spacing: 10pt body with ~1.3x leading (line height 5.2pt)
    BODY_LH = 5.2
    BODY_SIZE = 10
    PARAGRAPH_GAP = 2.5
    MAX_LINE_CHARS = 800

    # Pre-collect consecutive table rows so _render_table can size columns
    # correctly (requires all rows at once).
    table_buffer: list[list[str]] = []

    def flush_table():
        if table_buffer:
            _render_table(pdf, table_buffer[:])
            table_buffer.clear()
            pdf.ln(PARAGRAPH_GAP)
            pdf.set_text_color(*_COLOR_DEFAULT)
            pdf.set_font("Helvetica", "", BODY_SIZE)

    def write_body_paragraph(text: str, lh: float = BODY_LH, size: int = BODY_SIZE,
                              bold: bool = False, italic: bool = False,
                              color=None):
        pdf.set_text_color(*(color or _COLOR_DEFAULT))
        style = ("B" if bold else "") + ("I" if italic else "")
        pdf.set_font("Helvetica", style, size)
        pdf.multi_cell(0, lh, _safe(text), new_x="LMARGIN", new_y="NEXT")
        pdf.set_text_color(*_COLOR_DEFAULT)
        pdf.set_font("Helvetica", "", BODY_SIZE)

    for raw_line in md_content.splitlines():
        if len(raw_line) > MAX_LINE_CHARS:
            line = raw_line[:MAX_LINE_CHARS] + f" ... [+{len(raw_line) - MAX_LINE_CHARS} chars truncated]"
        else:
            line = raw_line

        # Flush the table buffer when we leave a run of pipe rows
        if not (line.strip().startswith("|") and line.strip().endswith("|")):
            flush_table()

        if line.startswith("# Network Security") or line.startswith("# Network Recon"):
            continue

        # Horizontal rule
        if line.strip() == "---":
            pdf.ln(2)
            pdf.set_draw_color(*_COLOR_RULE)
            y = pdf.get_y()
            pdf.line(pdf.l_margin, y, pdf.w - pdf.r_margin, y)
            pdf.ln(4)
            continue

        # Code block toggle
        if line.strip().startswith("```"):
            in_code_block = not in_code_block
            if in_code_block:
                pdf.set_font("Courier", "", 8)
            else:
                pdf.set_font("Helvetica", "", BODY_SIZE)
            continue

        # Code block content
        if in_code_block:
            pdf.set_fill_color(*_COLOR_CODE_BG)
            pdf.set_text_color(*_COLOR_DEFAULT)
            pdf.set_font("Courier", "", 8)
            safe = _safe(line)
            pdf.cell(0, 5, f"  {safe}", new_x="LMARGIN", new_y="NEXT", fill=True)
            continue

        # ## Headers — dark blue, bold, 14pt, with thin blue underline
        if line.startswith("## "):
            pdf.ln(4)
            pdf.set_text_color(*_COLOR_HEADER)
            pdf.set_font("Helvetica", "B", 14)
            pdf.cell(0, 8, _safe(line[3:]), new_x="LMARGIN", new_y="NEXT")
            pdf.set_draw_color(*_COLOR_HEADER)
            pdf.set_line_width(0.4)
            y = pdf.get_y()
            pdf.line(pdf.l_margin, y, pdf.l_margin + 60, y)
            pdf.set_line_width(0.2)
            pdf.ln(3)
            pdf.set_text_color(*_COLOR_DEFAULT)
            pdf.set_font("Helvetica", "", BODY_SIZE)
            continue

        # ### Sub-headers — medium blue, bold, 11pt
        if line.startswith("### "):
            pdf.ln(3)
            pdf.set_text_color(*_COLOR_SUBHEADER)
            pdf.set_font("Helvetica", "B", 11)
            pdf.cell(0, 6, _safe(line[4:]), new_x="LMARGIN", new_y="NEXT")
            pdf.ln(1)
            pdf.set_text_color(*_COLOR_DEFAULT)
            pdf.set_font("Helvetica", "", BODY_SIZE)
            continue

        # Table separator (skip)
        if line.strip().startswith("|--") or line.strip().startswith("|-"):
            continue

        # Table row: buffer it, render on flush
        if line.strip().startswith("|") and line.strip().endswith("|"):
            cells = [c.strip() for c in line.strip().strip("|").split("|")]
            table_buffer.append(cells)
            continue

        # Bullet points — body text black, severity label colored with badge
        if line.startswith("- "):
            text = re.sub(r"\*\*(.+?)\*\*", r"\1", line[2:])
            text = re.sub(r"`(.+?)`", r"\1", text)
            sev, rest = _extract_severity_badge(text)
            if sev:
                pdf.cell(4, BODY_LH, "-")
                _draw_severity_badge(pdf, sev)
                pdf.set_font("Helvetica", "", BODY_SIZE)
                pdf.set_text_color(*_COLOR_DEFAULT)
                pdf.multi_cell(0, BODY_LH, _safe(rest), new_x="LMARGIN", new_y="NEXT")
            else:
                pdf.set_font("Helvetica", "", BODY_SIZE)
                pdf.set_text_color(*_COLOR_DEFAULT)
                pdf.multi_cell(0, BODY_LH, "- " + _safe(text),
                               new_x="LMARGIN", new_y="NEXT")
            continue

        if line.startswith("  - "):
            text = line[4:]
            text = re.sub(r"\*\*(.+?)\*\*", r"\1", text)
            text = re.sub(r"`(.+?)`", r"\1", text)
            pdf.set_font("Helvetica", "", 9)
            pdf.set_text_color(*_COLOR_DEFAULT)
            pdf.multi_cell(0, 4.8, "    - " + _safe(text),
                           new_x="LMARGIN", new_y="NEXT")
            pdf.set_font("Helvetica", "", BODY_SIZE)
            continue

        # Lines that start with a bold label followed by value
        # (e.g. "**Risk Tier:** CRITICAL"). Keep the label dark-blue bold,
        # the value black. If the value is a severity word, show it as a badge.
        bold_match = re.match(r"\*\*(.+?)\*\*\s*(.*)", line)
        if bold_match:
            label = _safe(bold_match.group(1))
            value = bold_match.group(2).strip()
            value = re.sub(r"`(.+?)`", r"\1", value)

            pdf.set_text_color(*_COLOR_HEADER)
            pdf.set_font("Helvetica", "B", BODY_SIZE)
            label_w = pdf.get_string_width(label + " ") + 1
            pdf.cell(label_w, BODY_LH, label)

            sev_color = _severity_label_color(value) if value else None
            if sev_color:
                _draw_severity_badge(pdf, value)
                pdf.cell(0, BODY_LH, "", new_x="LMARGIN", new_y="NEXT")
            else:
                pdf.set_text_color(*_COLOR_DEFAULT)
                pdf.set_font("Helvetica", "", BODY_SIZE)
                pdf.multi_cell(0, BODY_LH, _safe(value),
                               new_x="LMARGIN", new_y="NEXT")
            continue

        # Italic / footer caption
        if line.startswith("*") and line.endswith("*") and len(line) > 2:
            pdf.ln(2)
            pdf.set_text_color(*_COLOR_DIM)
            pdf.set_font("Helvetica", "I", 8)
            pdf.multi_cell(0, 4.5, _safe(line.strip("*")),
                           new_x="LMARGIN", new_y="NEXT", align="C")
            pdf.set_text_color(*_COLOR_DEFAULT)
            pdf.set_font("Helvetica", "", BODY_SIZE)
            continue

        # Regular paragraph — black, no inline color
        if line.strip():
            text = re.sub(r"\*\*(.+?)\*\*", r"\1", line)
            text = re.sub(r"`(.+?)`", r"\1", text)
            pdf.set_text_color(*_COLOR_DEFAULT)
            pdf.set_font("Helvetica", "", BODY_SIZE)
            pdf.multi_cell(0, BODY_LH, _safe(text),
                           new_x="LMARGIN", new_y="NEXT")
            pdf.ln(PARAGRAPH_GAP)

    flush_table()  # flush any trailing table
    pdf.output(pdf_path)


# Common Unicode chars used in our markdown that latin-1 can't encode.
# Translate to readable ASCII equivalents instead of letting them turn into "?".
_UNICODE_FALLBACKS = {
    "\u2022": "*",   # bullet
    "\u2014": "-",   # em dash
    "\u2013": "-",   # en dash
    "\u2192": "->",  # rightwards arrow
    "\u2190": "<-",  # leftwards arrow
    "\u2713": "v",   # check mark
    "\u2717": "x",   # cross mark
    "\u2705": "[OK]",
    "\u274C": "[X]",
    "\u26A0": "!",   # warning
    "\u2026": "...", # ellipsis
    "\u201C": '"', "\u201D": '"',  # smart quotes
    "\u2018": "'", "\u2019": "'",
    # Box drawing chars used in menu banners / tables
    "\u2550": "=", "\u2551": "|",
    "\u2500": "-", "\u2502": "|",
    "\u250C": "+", "\u2510": "+",
    "\u2514": "+", "\u2518": "+",
    "\u251C": "+", "\u2524": "+",
    "\u252C": "+", "\u2534": "+", "\u253C": "+",
}


def _safe(text: str) -> str:
    """Make text safe for latin-1 PDF encoding.

    First substitute common Unicode chars (bullets, em dashes, box drawing)
    with ASCII equivalents so they render as expected, then fall back to
    'replace' for anything else (becomes '?').
    """
    for unicode_char, ascii_char in _UNICODE_FALLBACKS.items():
        if unicode_char in text:
            text = text.replace(unicode_char, ascii_char)
    return text.encode("latin-1", "replace").decode("latin-1")


