# ── Markdown — professional security assessment layout ─────────────────────

_SEVERITY_BADGE = {
    "CRITICAL": "[CRITICAL]",
    "HIGH":     "[HIGH]    ",
    "MEDIUM":   "[MEDIUM]  ",
    "LOW":      "[LOW]     ",
    "INFO":     "[INFO]    ",
}


def _risk_narrative(tier: str, total: int, counts: dict) -> str:
    """One-paragraph executive narrative driven by the overall tier."""
    crit = counts.get("CRITICAL", 0)
    high = counts.get("HIGH", 0)
    med = counts.get("MEDIUM", 0)
    if tier == "CRITICAL":
        return (
            f"The overall risk posture is **CRITICAL**. This assessment surfaced {total} finding(s) "
            f"across the in-scope hosts, including {crit} critical and {high} high-severity issue(s). "
            f"Immediate remediation of the critical findings is required; the affected hosts should be "
            f"considered actively at risk until the issues listed below are resolved."
        )
    if tier == "HIGH":
        return (
            f"The overall risk posture is **HIGH**. {high} high-severity and {med} medium-severity "
            f"finding(s) were identified. These should be addressed as part of the next scheduled "
            f"maintenance window; some findings may be exploitable by unauthenticated attackers."
        )
    if tier == "MEDIUM":
        return (
            f"The overall risk posture is **MEDIUM**. The assessment identified {med} medium-severity "
            f"finding(s) and no confirmed critical or high issues. Address the findings below in the "
            f"course of routine patching and configuration hardening."
        )
    if tier == "LOW":
        return (
            f"The overall risk posture is **LOW**. No critical, high, or medium-severity findings were "
            f"identified. The observations below are informational and represent general hardening "
            f"opportunities."
        )
    return (
        "The scan completed successfully but no actionable findings were generated by the rules engine. "
        "This typically means no open ports, services, or CVEs matched the assessment ruleset."
    )


def build_markdown() -> str:
    """Build a professional security-assessment Markdown report."""
    now = datetime.now().strftime("%Y-%m-%d %H:%M:%S")
    analysis = analyze_session()
    md: list[str] = []

    # ── Title / metadata ──
    md.append("# Network Security Assessment Report")
    md.append("")
    md.append(f"**Assessment Date:** {now}")
    md.append(f"**Tool:** Network Recon (nmap wrapper)")
    md.append(f"**Total Scans Performed:** {len(session_log)}")
    md.append("")
    md.append("---")
    md.append("")

    # ── Executive Summary ──
    md.append("## 1. Executive Summary")
    md.append("")
    md.append(_risk_narrative(
        analysis["overall_tier"],
        analysis["total_findings"],
        analysis["severity_counts"],
    ))
    md.append("")

    md.append("### 1.1 Findings by Severity")
    md.append("")
    md.append("| Severity | Count |")
    md.append("|----------|-------|")
    for sev in SEVERITY_ORDER:
        count = analysis["severity_counts"].get(sev, 0)
        if count or sev in ("CRITICAL", "HIGH", "MEDIUM"):
            md.append(f"| {sev} | {count} |")
    md.append(f"| **TOTAL** | **{analysis['total_findings']}** |")
    md.append("")

    md.append(f"**Overall Risk Level:** {analysis['overall_tier']}")
    md.append("")
    md.append("---")
    md.append("")

    # ── Scope & Methodology ──
    md.append("## 2. Scope & Methodology")
    md.append("")
    md.append("### 2.1 Scope")
    md.append("")
    if discovered_hosts:
        md.append(f"{len(discovered_hosts)} host(s) were identified as live and in-scope for detailed analysis:")
        md.append("")
        md.append("| # | IP | Hostname | Vendor / MAC |")
        md.append("|---|-----|----------|---------------|")
        for idx, h in enumerate(discovered_hosts, 1):
            hostname = h.get("hostname") or "-"
            vendor = h.get("vendor") or h.get("mac") or "-"
            md.append(f"| {idx} | {h['ip']} | {hostname} | {vendor} |")
        md.append("")
    else:
        targets = sorted({e.get("target") for e in session_log if e.get("target")})
        if targets:
            md.append("The following targets were specified for assessment:")
            md.append("")
            for t in targets:
                md.append(f"- {t}")
            md.append("")

    md.append("### 2.2 Methodology")
    md.append("")
    md.append(
        "The assessment used nmap-based active reconnaissance with the following techniques, "
        "depending on which scan modes were invoked during the session:"
    )
    md.append("")
    md.append("- **Host discovery** (`-sn`): ICMP/ARP ping sweep to identify live hosts on the target network.")
    md.append("- **Port enumeration** (`-p-`, `-sS`): Full TCP port scan using SYN probes.")
    md.append("- **Service and version detection** (`-sV`): Probe-based identification of service banners and versions.")
    md.append("- **Operating system fingerprinting** (`-O`): TCP/IP stack-based OS identification.")
    md.append("- **Vulnerability enumeration** (`--script vuln,vulners,exploit`): Nmap Scripting Engine (NSE) active checks for known CVEs and misconfigurations.")
    md.append("")
    md.append(
        "Findings were then processed by a rules-based analysis engine which correlates open ports, "
        "service banners, detected OS, CVE matches, and NSE vuln-check results against a curated list "
        "of known-risky services, end-of-life software, and default misconfigurations. Each finding is "
        "assigned a severity rating (CRITICAL / HIGH / MEDIUM / LOW / INFO) along with a human-written "
        "description and recommended remediation."
    )
    md.append("")
    md.append("### 2.3 Severity Rating Criteria")
    md.append("")
    md.append("| Rating | Definition |")
    md.append("|--------|------------|")
    md.append("| CRITICAL | Confirmed vulnerability, unauthenticated RCE, plaintext credentials on a reachable service, or default-credential database exposed to untrusted networks. Immediate action required. |")
    md.append("| HIGH | Serious exposure requiring prompt remediation: end-of-life software with known CVEs, exposed management interfaces, weak authentication protocols. |")
    md.append("| MEDIUM | Meaningful hardening issue: information leakage, deprecated protocols, weak configurations that enable secondary attacks. |")
    md.append("| LOW | Informational exposure: expected services that still warrant review for least-privilege configuration. |")
    md.append("| INFO | Observation with no direct security impact. |")
    md.append("")
    md.append("---")
    md.append("")

    # ── Host Risk Matrix ──
    hosts_with_findings = analysis["hosts"]
    if hosts_with_findings:
        md.append("## 3. Host Risk Matrix")
        md.append("")
        md.append("| Host | Hostname / Vendor | Risk Tier | Score | Open Ports | CVEs | Findings |")
        md.append("|------|-------------------|-----------|-------|------------|------|----------|")
        for h in hosts_with_findings:
            label = h["hostname"] or h["vendor"] or "-"
            md.append(
                f"| {h['host']} | {label} | {h['tier']} | {h['score']} | "
                f"{h['open_port_count']} | {h['cve_count']} | {len(h['findings'])} |"
            )
        md.append("")
        md.append("*Host risk tiers are derived from the worst-severity finding on each host. "
                  "Scores are a weighted sum of all findings (CRITICAL=10, HIGH=5, MEDIUM=2, LOW=0.5).*")
        md.append("")
        md.append("---")
        md.append("")

    # ── Detailed Findings per Host ──
    if any(h["findings"] for h in hosts_with_findings):
        md.append("## 4. Detailed Findings")
        md.append("")
        md.append(
            "Findings are grouped by host, ordered by severity. Each finding includes a description "
            "of the issue, the evidence observed, and a specific remediation recommendation."
        )
        md.append("")

        for h in hosts_with_findings:
            if not h["findings"]:
                continue

            label_parts = [h["host"]]
            if h["hostname"]:
                label_parts.append(h["hostname"])
            if h["vendor"]:
                label_parts.append(h["vendor"])
            header_label = " — ".join(label_parts)

            md.append(f"### 4.{hosts_with_findings.index(h) + 1} {header_label}")
            md.append("")
            md.append(f"- **Risk Tier:** {h['tier']}")
            md.append(f"- **Weighted Score:** {h['score']}")
            md.append(f"- **Open Ports:** {h['open_port_count']}")
            md.append(f"- **CVE Catalog Matches:** {h['cve_count']}")
            md.append("")

            for idx, finding in enumerate(h["findings"], 1):
                md.append(f"### Finding {idx}: [{finding['severity']}] {finding['title']}")
                md.append("")
                md.append(f"- **Endpoint:** `{finding['endpoint']}`")
                md.append(f"- **Evidence:** {finding['evidence']}")
                md.append("")
                md.append("**Description:**")
                md.append("")
                md.append(finding["description"])
                md.append("")
                md.append("**Recommendation:**")
                md.append("")
                md.append(finding["recommendation"])
                md.append("")

            md.append("---")
            md.append("")

    # ── Remediation Roadmap ──
    if analysis["total_findings"]:
        md.append("## 5. Remediation Roadmap")
        md.append("")
        md.append(
            "The following priority list consolidates every finding across all hosts, ordered by "
            "severity. Items should be addressed top-to-bottom."
        )
        md.append("")

        flat: list[dict] = []
        for h in hosts_with_findings:
            for f in h["findings"]:
                flat.append(f)
        flat.sort(key=lambda f: (SEVERITY_ORDER.index(f["severity"]), f["title"]))

        priority = 1
        current_sev = None
        for finding in flat:
            if finding["severity"] != current_sev:
                if current_sev is not None:
                    md.append("")
                current_sev = finding["severity"]
                md.append(f"### {current_sev} priority")
                md.append("")
            md.append(f"{priority}. **{finding['title']}** — `{finding['endpoint']}`")
            md.append(f"   - {finding['recommendation']}")
            priority += 1
        md.append("")
        md.append("---")
        md.append("")

    # ── Technical Appendix ──
    md.append("## 6. Technical Appendix")
    md.append("")
    md.append("This appendix contains the raw technical data collected during the assessment.")
    md.append("")

    # A. CVE list
    all_cves = sorted({c for h in hosts_with_findings for c in [] for _ in range(0)})
    all_cves = sorted({cve for entry in session_log
                       for cve in entry.get("findings", {}).get("cves", []) or []})
    if all_cves:
        md.append("### 6.1 CVE Catalog Matches")
        md.append("")
        md.append(
            "The following CVE identifiers were reported by the nmap `vulners` script based on "
            "service-banner matching. These represent *potential* issues associated with the detected "
            "software versions and should be confirmed against vendor advisories before assuming "
            "exploitability."
        )
        md.append("")
        md.append("| # | CVE ID |")
        md.append("|---|--------|")
        for idx, cve in enumerate(all_cves, 1):
            md.append(f"| {idx} | {cve} |")
        md.append("")

    # B. Open ports (aggregated)
    all_ports = []
    for entry in session_log:
        all_ports.extend(entry.get("findings", {}).get("open_ports", []) or [])
    unique_ports = sorted(set(all_ports))
    if unique_ports:
        md.append("### 6.2 Open Ports & Services Inventory")
        md.append("")
        md.append("| Port | State | Service / Version |")
        md.append("|------|-------|-------------------|")
        for p in unique_ports:
            parts = p.split(None, 2)
            if len(parts) >= 3:
                md.append(f"| {parts[0]} | {parts[1]} | {parts[2]} |")
            else:
                md.append(f"| {p} | | |")
        md.append("")

    # C. OS detections
    all_os = sorted({os_line for entry in session_log
                     for os_line in entry.get("findings", {}).get("os_detected", []) or []})
    if all_os:
        md.append("### 6.3 Operating Systems Detected")
        md.append("")
        for os_line in all_os:
            md.append(f"- {os_line}")
        md.append("")

    # D. Per-scan log
    md.append("### 6.4 Scan Log")
    md.append("")
    md.append("Chronological log of the individual nmap invocations performed during this session.")
    md.append("")
    for i, entry in enumerate(session_log, 1):
        status = "Success" if entry.get("return_code") == 0 else "Failed / Interrupted"
        md.append(f"**Scan #{i}:** {entry.get('description', '(no description)')}")
        md.append("")
        md.append(f"- **Time:** {entry.get('time', '-')}")
        md.append(f"- **Status:** {status}")
        md.append(f"- **Target:** {entry.get('target', '-')}")
        md.append("- **Command:**")
        md.append("  ```")
        md.append(f"  {entry.get('command', '-')}")
        md.append("  ```")
        if entry.get("output_file"):
            md.append(f"- **Raw output:** `{entry['output_file']}`")

        f = entry.get("findings", {})
        if isinstance(f, dict):
            if f.get("hosts_up"):
                md.append(f"- **Hosts up:** {f['hosts_up']}")
            if f.get("open_ports"):
                md.append(f"- **Open ports:** {len(f['open_ports'])}")
            if f.get("cves"):
                md.append(f"- **CVE catalog matches:** {len(f['cves'])}")
            scan_passed = f.get("vulns_passed", 0)
            scan_failed = f.get("vulns_failed") or []
            scan_likely = f.get("vulns_likely") or []
            if scan_failed or scan_likely:
                md.append("- **NSE vulnerability checks:**")
                for v in scan_failed:
                    md.append(f"  - [VULNERABLE] `{v['script']}` on `{v['endpoint']}` -> {v['description']}")
                for v in scan_likely:
                    md.append(f"  - [LIKELY] `{v['script']}` on `{v['endpoint']}` -> {v['description']}")
                if scan_passed:
                    md.append(f"  - *({scan_passed} additional checks passed)*")
            elif scan_passed:
                md.append(f"- **NSE vulnerability checks:** {scan_passed} ran, all passed")
        md.append("")

    md.append("---")
    md.append("")

    # ── Disclaimer ──
    md.append("## 7. Disclaimer")
    md.append("")
    md.append(
        "This assessment was generated by an automated tool and reflects the state of the target(s) "
        "at the time of scanning. It is not a substitute for a manual penetration test or a full "
        "code/configuration audit. Findings based on service-banner version matching (CVE catalog) may "
        "produce false positives if the vendor has backported security fixes without changing the "
        "advertised version string."
    )
    md.append("")
    md.append(
        "**Authorization:** Active network scanning of systems you do not own or have explicit "
        "written permission to test is illegal in most jurisdictions. This report should only be "
        "distributed to parties with a legitimate need to know."
    )
    md.append("")
    md.append("---")
    md.append("")
    md.append("*Report generated by Network Recon*")

    return "\n".join(md)


